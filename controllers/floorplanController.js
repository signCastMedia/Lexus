//UPDATE FLOORPLAN
async function updateFloor(req, res) {
  const {
    floorId,
    productName,
    subtitle,
    type,
    sqFt,
    exterior,
     sqFtOut,
    exposure,
    numOfBed,
    bath,
    bf,
    thumbnail,
    thumbnailSmall,
    gallery,
    hide,
  } = req.body;


  try {
    let floorplan = await FloorPlan.findById(floorId);
    floorplan.productName = productName ? productName : floorplan.productName;
    floorplan.subtitle = subtitle ? subtitle : floorplan.subtitle;
    floorplan.thumbnail = thumbnail ? thumbnail : floorplan.thumbnail;
    floorplan.thumbnailSmall = thumbnailSmall ? thumbnailSmall : floorplan.thumbnailSmall;
    floorplan.gallery = gallery ? gallery : floorplan.gallery;
    floorplan.numOfBed = numOfBed ? numOfBed : floorplan.numOfBed;
    floorplan.sqFt = sqFt ? sqFt : floorplan.sqFt;
    floorplan.sqFtOut = sqFtOut ? sqFtOut : floorplan.sqFtOut;
    floorplan.exterior = exterior ? exterior : floorplan.exterior;
    floorplan.exposure = exposure ? exposure : floorplan.exposure;
    floorplan.type = type ? type : floorplan.type;
    floorplan.bf = bf ? bf : floorplan.bf;
    floorplan.bath = bath ? bath : floorplan.bath;
    floorplan.hide = hide 
    await floorplan.save();

    res.status(200);
    res.json("Successfully updated");
  } catch (err) {
    res.status(400);
    res.json({ 'Error': err });
  }
}










